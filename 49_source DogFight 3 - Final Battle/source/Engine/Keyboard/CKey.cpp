//----------------------------------------------------------------------------------------------
bool CKey::Key_Down(char Key)
//----------------------------------------------------------------------------------------------
  {
  return ((GetAsyncKeyState(Key) & 0x8000) ? 1 : 0); // Chekker den højeste bit som bestemmer tasten oppe/nedde placering. (0x7FFF indholder tasten som blev kaldt)
  }